<?php
require_once 'database.php';

$error = '';
$success = '';

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $admin_id = trim($_POST['admin_id']);
    $first_name = trim($_POST['first_name']);
    $middle_name = trim($_POST['middle_name']);
    $last_name = trim($_POST['last_name']);
    $password = $_POST['password'];
    $confirm_password = $_POST['confirm_password'];

    // Validation
    if (empty($admin_id) || empty($first_name) || empty($last_name) || empty($password) || empty($confirm_password)) {
        $error = "All fields are required";
    } elseif ($password !== $confirm_password) {
        $error = "Passwords do not match";
    } else {
        // Check if admin_id already exists
        $stmt = $conn->prepare("SELECT id FROM admins WHERE admin_id = ?");
        $stmt->execute([$admin_id]);
        
        if ($stmt->rowCount() > 0) {
            $error = "Admin ID already exists";
        } else {
            // Hash password
            $hashed_password = password_hash($password, PASSWORD_DEFAULT);
            
            // Insert new admin
            $stmt = $conn->prepare("INSERT INTO admins (admin_id, first_name, middle_name, last_name, password) VALUES (?, ?, ?, ?, ?)");
            
            try {
                $stmt->execute([$admin_id, $first_name, $middle_name, $last_name, $hashed_password]);
                $success = "Registration successful! You can now login.";
            } catch(PDOException $e) {
                $error = "Registration failed: " . $e->getMessage();
            }
        }
    }
}
